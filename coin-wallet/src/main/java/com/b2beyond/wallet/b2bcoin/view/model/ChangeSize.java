package com.b2beyond.wallet.b2bcoin.view.model;

public class ChangeSize {

    private int width;
    private int height;

    public ChangeSize(int width, int height) {
        this.width = width;
        this.height = height;
    }

    public int getWidth() {
        return width;
    }

    public int getHeight() {
        return height;
    }

}
